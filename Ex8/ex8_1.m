I=imread('DIP.jpg');
subplot(1,3,1);
imshow(I);
[row,col]=size(I);
[u,v]=meshgrid(-row/2+1:row/2,-col/2+1:col/2);
T=1;
% r=0.1/2/pi;
r=0.1;
a=(1+2^(1/2)/2)*r;
b=2^(1/2)/2*r;
H=T/pi./(u*a+v*b).*sin(pi.*(u*a+v*b)).*exp(-j*pi*(u*a+v*b));
I_=fft2(I);
I_=fftshift(I_);
G=H.*I_;
Ih=ifft2(fftshift(G));
Ih=real(Ih);
subplot(1,3,2);
imshow(Ih,[0,255]);
k=40;
H2=exp(-k*((u-row/2)^2+(v-col/2)^2)^5/6);
G2=H2.*G;
Ih2=ifft2(fftshift(G2));
Ih2=real(Ih2);
subplot(1,3,3);
imshow(Ih2,[0,255]);
err1=immse(Ih,I);
err2=immse(Ih2,I);

